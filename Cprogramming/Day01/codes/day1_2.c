// Variable, declaration, assigning and printing

#include<stdio.h>

int main()
{
    int num1; // variable declaration
    num1 = 20; // assigning the value

    int num2 = 35; // variable initialization

    // output : num1 = 20

    printf("num1 = %d\n",num1);

    printf("num2 : %d",num2);
    return 0;
}